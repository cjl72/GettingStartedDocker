import math
class Calculator:
    result = 0

    def __init__(self):
        pass

    def addition(self, a, b):
        return a + b

    def subtraction(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def division(self, a, b):
        return a / b

    def squareroot(self, a):
        return math.sqrt(a)

    def square(self, a):
        return a * a